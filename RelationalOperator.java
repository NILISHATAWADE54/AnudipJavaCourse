package operator;

public class RelationalOperator {

	public static void main(String[] args) {
		int x=10;
	    int y=20;
	    boolean rel=x<=y;
	    System.out.println(rel);
        boolean res=x==y;
        System.out.println(res);
        boolean r=x!=y;
        System.out.println(r);
        boolean n=x>=y;
        System.out.println(n);
	}

}
